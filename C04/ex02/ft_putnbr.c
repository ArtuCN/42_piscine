#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	d;
	long	i;

	i = nb;
	if (i < 0)
	{
		write (1, "-", 1);
		i = i * -1;
	}
	d = 48 +(i % 10);
	if (i >= 10)
		ft_putnbr (i / 10);
	write (1, &d, 1);
}
